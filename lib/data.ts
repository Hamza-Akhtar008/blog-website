// Function to get related posts based on category and tags
export function getRelatedPosts(postId: string, category: string, tags: string[], limit = 3) {
  return blogPosts
    .filter((post) => post.id !== postId && (post.category === category || post.tags.some((tag) => tags.includes(tag))))
    .sort((a, b) => {
      // Count matching tags
      const aMatchingTags = a.tags.filter((tag) => tags.includes(tag)).length
      const bMatchingTags = b.tags.filter((tag) => tags.includes(tag)).length

      // Sort by matching tags count (descending)
      return bMatchingTags - aMatchingTags
    })
    .slice(0, limit)
}

// Function to get posts by category
export function getPostsByCategory(category: string) {
  return blogPosts.filter((post) => post.category.toLowerCase() === category.toLowerCase())
}

// Function to search posts
export function searchPosts(query: string) {
  const searchTerms = query.toLowerCase().split(" ")

  return blogPosts.filter((post) => {
    const titleMatch = searchTerms.some((term) => post.title.toLowerCase().includes(term))
    const contentMatch = searchTerms.some((term) => post.content.toLowerCase().includes(term))
    const excerptMatch = searchTerms.some((term) => post.excerpt.toLowerCase().includes(term))
    const categoryMatch = searchTerms.some((term) => post.category.toLowerCase().includes(term))
    const tagMatch = searchTerms.some((term) => post.tags.some((tag) => tag.toLowerCase().includes(term)))

    return titleMatch || contentMatch || excerptMatch || categoryMatch || tagMatch
  })
}

